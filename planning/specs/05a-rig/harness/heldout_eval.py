"""E2 — the held-out go/no-go for the 05d router.

The probe_05d act-type gate regex was written while looking at dataset.json, so
its 80% is potentially overfit. This builds a HELD-OUT set — fresh utterances
generated by Sonnet (deliberately NOT Haiku, which wrote the synthetic anchors,
to avoid same-model style leakage) — and scores the FROZEN router (identical
PHRASES, anchors, gate regex, imported from probe_05d) on it, once.

Reliable labels: each utterance's label is the capability it was generated for;
OOD utterances are labelled `none`. Usage: heldout_eval.py [embed_port=8091]
"""
import json, os, sys
sys.stdout.reconfigure(encoding="utf-8")
sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
import lib          # noqa: E402
import probe_05d as p  # noqa: E402  (frozen router: embed, act_type, PHRASES, score_config, report)

PORT = sys.argv[1] if len(sys.argv) > 1 else "8091"
p.EMBED_URL = f"http://127.0.0.1:{PORT}/v1/embeddings"
p._embed_cache = {}
RIG = lib.RIG
HELDOUT = os.path.join(RIG, "heldout.json")
GEN_MODEL = "claude-sonnet-4-5"   # NOT haiku (the anchor generator)

GEN_SYS = (
    "You generate a HELD-OUT test set for a voice assistant's intent router. Given one "
    "capability, write short, natural, DIVERSE utterances a real user would SAY OUT LOUD "
    "to invoke it. Vary register: terse fragments, full casual sentences, at least one "
    "with genuine disfluency (um, uh, like, i guess), concrete slot values (real names, "
    "numbers, dates, times). Make them realistically messy, NOT template-like, and do not "
    "restate the capability name. One utterance per line; no numbering, quotes, or commentary."
)
OOD_SYS = (
    "Write short spoken questions a user might ask a phone assistant that CANNOT be answered "
    "from the user's OWN personal notes, logs, trackers, or contacts — general knowledge / "
    "world questions (weather, sports scores, facts, definitions, arithmetic, directions). "
    "One per line; no numbering or commentary."
)


def _lines(text, n):
    return [ln.strip(" -\"'\t") for ln in text.splitlines() if ln.strip()][:n]


def gen_heldout(skills_meta):
    if os.path.exists(HELDOUT):
        return json.load(open(HELDOUT, encoding="utf-8"))
    cases, cost = [], 0.0
    for sid, meta in skills_meta.items():
        user = (f"Capability: {meta['desc']}\nSlots: {', '.join(meta['slots']) or '(none)'}\n"
                f"Write 3 utterances.")
        r = lib.claude_chat(GEN_MODEL, GEN_SYS, user, max_tokens=220)
        cost += r["cost_usd"]
        for ln in _lines(r["text"], 3):
            if 2 <= len(ln) <= 140:
                cases.append({"id": f"H-{sid}-{len([c for c in cases if c['expected']['skillId']==sid])+1}",
                              "utterance": ln, "expected": {"skillId": sid}, "class": "A"})
    r = lib.claude_chat(GEN_MODEL, OOD_SYS, "Write 8.", max_tokens=220)
    cost += r["cost_usd"]
    for i, ln in enumerate(_lines(r["text"], 8)):
        if 2 <= len(ln) <= 140:
            cases.append({"id": f"H-ood-{i+1}", "utterance": ln,
                          "expected": {"skillId": "none"}, "class": "D"})
    json.dump({"cases": cases, "gen_model": GEN_MODEL, "gen_cost": round(cost, 4)},
              open(HELDOUT, "w", encoding="utf-8"), indent=1)
    print(f"generated {len(cases)} held-out cases via {GEN_MODEL} (${cost:.3f}) -> heldout.json")
    return {"cases": cases}


def main():
    ds = json.load(open(os.path.join(RIG, "dataset.json"), encoding="utf-8"))
    skills_meta = {}
    for c in ds["cases"]:
        for cand in c.get("candidates", []):
            skills_meta.setdefault(cand["skillId"],
                                   {"desc": cand.get("desc", ""), "slots": cand.get("slots", [])})
    all_ids = set(skills_meta)
    cases = gen_heldout(skills_meta)["cases"]
    nreal = sum(1 for c in cases if c["expected"]["skillId"] != "none")
    print(f"{len(cases)} held-out cases ({nreal} real-skill, {len(cases)-nreal} OOD)\n")

    name_desc = {sid: f"{sid.replace('-', ' ')}. {m['desc']}" for sid, m in skills_meta.items()}
    anchors = json.load(open(os.path.join(RIG, "results", "probe-synthetic-anchors.json"), encoding="utf-8"))
    vec_multi = {sid: [p.embed(name_desc[sid])] + [p.embed(x) for x in p.PHRASES.get(sid, [])] for sid in all_ids}
    vec_syn = {sid: vec_multi[sid] + [p.embed(x) for x in anchors.get(sid, [])] for sid in all_ids}

    print("== HELD-OUT (frozen router; utterances by Sonnet; gate regex UNCHANGED) ==")
    res = {}
    res["multi"] = p.report("HO multi", p.score_config(cases, vec_multi, all_ids))
    res["syn"] = p.report("HO syn", p.score_config(cases, vec_syn, all_ids))
    rows_sg = p.score_config(cases, vec_syn, all_ids, gate=True)
    res["syn_gate"] = p.report("HO syn+gate", rows_sg)

    print("\ngate exclusions on held-out (correct skill wrongly gated out — the overfit tell):")
    gx = [r for r in rows_sg if r.get("gate_excluded")]
    for r in gx:
        print(f"  {r['id']:<18} exp={r['exp']:<22} act_type={p.act_type([c['utterance'] for c in cases if c['id']==r['id']][0])}")
    print(f"  total gate-excluded: {len(gx)}")

    json.dump({k: {kk: vv for kk, vv in v.items() if kk != 'rows'} for k, v in res.items()},
              open(os.path.join(RIG, "results", "heldout-eval.json"), "w"), indent=1)
    print("\n-> results/heldout-eval.json")


if __name__ == "__main__":
    main()
